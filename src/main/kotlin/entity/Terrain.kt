package entity

/**
 * Enum [Terrain] representing the different terrain types in the Cascadia game.
 * Each terrain type is associated with specific habitat tiles.
 *
 * @property FOREST Represents a forest terrain.
 * @property MOUNTAIN Represents a mountain terrain.
 * @property PRAIRIE Represents a prairie terrain.
 * @property RIVER Represents a river terrain.
 * @property WETLAND Represents a wetland terrain.
 */
enum class Terrain {
    FOREST,
    MOUNTAIN,
    PRAIRIE,
    RIVER,
    WETLAND;
}