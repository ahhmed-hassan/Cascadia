package entity

/**
 * Enum [Animal] representing the types of animals found in the Cascadia game.
 * Each animal has specific behaviors and scoring rules defined in the game.
 *
 * @property BEAR Represents a bear in the game.
 * @property ELK Represents an elk in the game.
 * @property FOX Represents a fox in the game.
 * @property HAWK Represents a hawk in the game.
 * @property SALMON Represents a salmon in the game.
 */
enum class Animal {
    BEAR,
    ELK,
    FOX,
    HAWK,
    SALMON;
}